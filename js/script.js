        // Function to update greeting based on time of day and user's name
        function updateGreeting() {
            const hour = new Date().getHours();
            const greetingElement = document.getElementById('welcome-greeting');
            const userName = localStorage.getItem('userName') || '';
            let greeting;
            
            // Determine time-based greeting
            if (hour < 12) {
                greeting = currentLang === 'id' ? "Selamat Pagi" : "Good Morning";
            } else if (hour < 18) {
                greeting = currentLang === 'id' ? "Selamat Siang" : "Good Afternoon";
            } else {
                greeting = currentLang === 'id' ? "Selamat Malam" : "Good Evening";
            }
            
            // Construct final greeting
            let fullGreeting;
            if (userName) {
                fullGreeting = `${greeting} ${userName}, Welcome To <span class='text-[#ff2a6d] [text-shadow:_0_0_10px_#ff2a6d,_0_0_20px_#ff2a6d]'>Hacktiv8 x IBM</span>`;
            } else {
                fullGreeting = `${greeting}, Welcome To <span class='text-[#ff2a6d] [text-shadow:_0_0_10px_#ff2a6d,_0_0_20px_#ff2a6d]'>Hacktiv8 x IBM</span>`;
            }
            
            greetingElement.innerHTML = fullGreeting;
        }

        // Function to change language
        function changeLanguage(lang) {
            currentLang = lang;
            localStorage.setItem('preferredLanguage', lang);
            
            // Update all elements with language classes
            Object.keys(translations[lang]).forEach(key => {
                const elements = document.querySelectorAll(`.lang-${key}`);
                elements.forEach(element => {
                    element.textContent = translations[lang][key];
                });
                
                // Handle placeholders
                if (key.includes('Placeholder')) {
                    const inputElements = document.querySelectorAll(`[placeholder="${translations[currentLang === 'en' ? 'id' : 'en'][key]}"]`);
                    inputElements.forEach(element => {
                        element.setAttribute('placeholder', translations[lang][key]);
                    });
                }
            });
            
            // Update active language button
            document.querySelectorAll('.language-option').forEach(option => {
                option.classList.remove('active');
                if (option.dataset.lang === lang) {
                    option.classList.add('active');
                }
            });
            
            // Update greeting with new language
            updateGreeting();
        }

        // Welcome modal functionality
        function showWelcomeModal() {
            const welcomeModal = document.getElementById('welcome-modal');
            const userNameInput = document.getElementById('user-name-input');
            const submitNameBtn = document.getElementById('submit-name');
            const welcomeTitle = document.getElementById('welcome-title');
            const welcomeMessage = document.getElementById('welcome-message');
            
            // Check if user has already submitted their name
            if (!localStorage.getItem('userName')) {
                // Set initial language for modal
                welcomeTitle.textContent = translations[currentLang].welcomeTitle;
                welcomeMessage.textContent = translations[currentLang].welcomeMessage;
                userNameInput.placeholder = currentLang === 'id' ? "Nama Anda" : "Your name";
                submitNameBtn.textContent = currentLang === 'id' ? "Lanjutkan" : "Continue";
                
                welcomeModal.style.display = 'flex';
                
                submitNameBtn.addEventListener('click', function() {
                    const userName = userNameInput.value.trim();
                    if (userName) {
                        localStorage.setItem('userName', userName);
                        welcomeModal.style.opacity = '0';
                        setTimeout(() => {
                            welcomeModal.style.display = 'none';
                        }, 500);
                        updateGreeting();
                    }
                });
                
                // Also allow submission on Enter key
                userNameInput.addEventListener('keypress', function(e) {
                    if (e.key === 'Enter') {
                        const userName = userNameInput.value.trim();
                        if (userName) {
                            localStorage.setItem('userName', userName);
                            welcomeModal.style.opacity = '0';
                            setTimeout(() => {
                                welcomeModal.style.display = 'none';
                            }, 500);
                            updateGreeting();
                        }
                    }
                });
            } else {
                welcomeModal.style.display = 'none';
            }
        }

        // Initialize language switcher
        document.addEventListener('DOMContentLoaded', function() {
            // Check for preferred language in localStorage
            const preferredLanguage = localStorage.getItem('preferredLanguage');
            if (preferredLanguage) {
                changeLanguage(preferredLanguage);
            } else {
                // Default to Indonesian
                changeLanguage('id');
            }
            
            // Show welcome modal
            showWelcomeModal();
            
            // Language switcher toggle
            const languageBtn = document.getElementById('language-btn');
            const languageOptions = document.getElementById('language-options');
            
            languageBtn.addEventListener('click', function() {
                languageOptions.classList.toggle('show');
            });
            
            // Language option selection
            document.querySelectorAll('.language-option').forEach(option => {
                option.addEventListener('click', function() {
                    const lang = this.dataset.lang;
                    changeLanguage(lang);
                    languageOptions.classList.remove('show');
                    
                    // Update welcome modal if it's visible
                    if (document.getElementById('welcome-modal').style.display === 'flex') {
                        const welcomeTitle = document.getElementById('welcome-title');
                        const welcomeMessage = document.getElementById('welcome-message');
                        const userNameInput = document.getElementById('user-name-input');
                        const submitNameBtn = document.getElementById('submit-name');
                        
                        welcomeTitle.textContent = translations[lang].welcomeTitle;
                        welcomeMessage.textContent = translations[lang].welcomeMessage;
                        userNameInput.placeholder = lang === 'id' ? "Nama Anda" : "Your name";
                        submitNameBtn.textContent = lang === 'id' ? "Lanjutkan" : "Continue";
                    }
                });
            });
            
            // Close language options when clicking outside
            document.addEventListener('click', function(event) {
                if (!languageBtn.contains(event.target)) {
                    languageOptions.classList.remove('show');
                }
            });
            
            // Call the greeting function when page loads
            updateGreeting();
        });

        // Update greeting every minute in case the time changes (morning to afternoon, etc.)
        setInterval(updateGreeting, 60000);

        // Function to update active navigation link
        function updateActiveNavLink() {
            const navLinks = document.querySelectorAll('.nav-link');
            const sections = document.querySelectorAll('.page-section');
            let currentSection = '';
            
            // Get current section in view
            sections.forEach(section => {
                const sectionTop = section.offsetTop;
                const sectionHeight = section.clientHeight;
                if (window.scrollY >= (sectionTop - 100) && window.scrollY < (sectionTop + sectionHeight - 100)) {
                    currentSection = section.getAttribute('id');
                }
            });
            
            // Update active link
            navLinks.forEach(link => {
                link.classList.remove('active');
                if (link.getAttribute('href') === `#${currentSection}`) {
                    link.classList.add('active');
                    
                    // Change header color based on section
                    const header = document.querySelector('.main-header');
                    switch(currentSection) {
                        case 'home':
                            header.style.borderBottomColor = '#05d9e8';
                            break;
                        case 'profile':
                            header.style.borderBottomColor = '#00ff9d';
                            break;
                        case 'portfolio':
                            header.style.borderBottomColor = '#ff2a6d';
                            break;
                        case 'services':
                            header.style.borderBottomColor = '#d300c5';
                            break;
                        case 'team':
                            header.style.borderBottomColor = '#05d9e8';
                            break;
                        case 'message-us':
                            header.style.borderBottomColor = '#00ff9d';
                            break;
                        default:
                            header.style.borderBottomColor = '#05d9e8';
                    }
                }
            });
        }

